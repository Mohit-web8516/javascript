//A Promise in JavaScript is an object that represents the eventual completion (success) or failure of an asynchronous operation. It acts as a placeholder for a value that will be available in the future. Promises help manage asynchronous tasks like API calls, file reading, or timers in a cleaner way than traditional callbacks.



const promiseOne = new Promise(function(resolve, reject){
    //Do an async task
    // DB calls, cryptography, network
    setTimeout(function(){
        console.log('Async task is compelete');
        resolve() 
    }, 1000)
})

promiseOne.then(function(){
    console.log("Promise consumed");
})

